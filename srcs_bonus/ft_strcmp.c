#include "../includes/push_swap.h"

bool	ft_strcmp(char *s1, char*s2)
{
	unsigned int	i;

	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && s1[i] != '\n')
	{
		if (s1[i] != s2[i])
			return (false);
		i++;
	}
	return (true);
}
